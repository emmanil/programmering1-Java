//Nedan räknar upp räknare och summor vänster + höger sida.   spelare 1. 
private void raknaIhopSumma1OchSumma2InklBonusSpelare1(){
    
    raknareAntalSvarSpelare1++;
    
    
         if (raknareAntalSvarSpelare1 == antalRutorSomFinnsSpelare1){
            
        ettor =txfEttor.getText(); //hämtar string
        tvaor =txfTvaor.getText();
        treor =txfTreor.getText();
        fyror =txfFyror.getText();
        femmor =txfFemmor.getText();
        sexor =txfSexor.getText();
        
        int seOmBonus = (Integer.valueOf(ettor))+(Integer.valueOf(tvaor))+(Integer.valueOf(treor))+
            +(Integer.valueOf(fyror))+(Integer.valueOf(femmor))+(Integer.valueOf(sexor));
        if (seOmBonus > 62){ //vid 63 poäng ges bonus.
            txfBonus.setText("50");
            }
        else {
            txfBonus.setText("0");
        }

        String bonus = txfBonus.getText();
                  
        summa1Spelare1 =(Integer.valueOf(ettor))+(Integer.valueOf(tvaor))+(Integer.valueOf(treor))+
        +(Integer.valueOf(fyror))+(Integer.valueOf(femmor))+(Integer.valueOf(sexor))+
        (Integer.valueOf(bonus));
        
        txfSummaInklBonus.setText(Integer.toString(summa1Spelare1));
        }
         
       if (raknareAntalSvarSpelare1 == antalRutorSomFinnsSpelare1){  
       
        int summa2 =  (Integer.valueOf(txfEttPar.getText())) +
        (Integer.valueOf(txfTvapar.getText())) + (Integer.valueOf(txfTretal.getText())) + 
        (Integer.valueOf(txfFyrtal.getText())) + (Integer.valueOf(txfLitenStege.getText())) +
        (Integer.valueOf(txfStorStege.getText())) + (Integer.valueOf(txfKak.getText())) +
        (Integer.valueOf(txfYatzy.getText())) + (Integer.valueOf(txfChans.getText()));
    
    
        int slutsumma = summa1Spelare1 + summa2;
                    
        txfSumma2.setText(Integer.toString(slutsumma));
       }                
}

