public void sorteraTarningarLitenTillStor (int [] dice) {   //infogande sortering ex: 01234  
    int i;
    int temp;
       //bubblesortering.
for (int n=1 ; n < dice.length ; n++) {   //start 1 ; stopp vid dice längd ; +1
temp = dice [n]; // index n är temp
i = n -1;

    while (( i >= 0 ) && ( dice[i] > temp)){ //i är större än/lika med 0 OCH dice[n] STÖRRE ÄN temp
    dice[i+1] = dice[i];      //förskjut åt höger
    i--; //jag tittar på i ett steg till vänster
        }
    dice[i+1] = temp; //infoga på den tomma platsen
    }
}

